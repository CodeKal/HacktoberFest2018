a = int(input("1.sayı: "))

# "%" operatörü bir sayısının diğer sayıya bölümünden kalan sonucu verir.
if a%3==0:
   print(a,"3'e bölünür.")
else:
   print(a, "sayı 3'e bölünmez.")

